import pygtrie
class Lexicon:
    def __init__(self):
        file = open('src/Game/fise-2.txt', 'r')
        words = file.read().split('\n')
        self.trie = pygtrie.CharTrie()
        for word in words:
            self.trie[word] = True

    def validate(self, word : str) -> bool:
        return self.trie.has_key(word)

